"""シンプルな白地図スタイルの日本地図生成プログラム

このプログラムは、japanmapライブラリを使用して、境界線のみを強調した
シンプルな白地図スタイルの日本地図を生成します。地図は教育用途や
プレゼンテーションなどで使いやすい、すっきりとしたデザインを特徴としています。

プログラムの動作の詳細：
プログラムは以下の手順で地図を生成します：

1. まず、japanmapライブラリから都道府県の基本データを取得します。これには
  都道府県コード（1から47）と都道府県名の対応情報が含まれています。

2. 次に、地図の色分けデータを作成します。このプログラムでは、すべての
  都道府県を白色（'white'）に設定し、均一な背景を作成します。これにより、
  境界線が際立つ視覚効果を得ることができます。

3. 最後に、matplotlib を使用して地図を描画します。特徴的な点として：
  - グレースケールの境界線を使用し、やわらかい印象を与えます
  - 透明度（alpha）を0.3に設定し、境界線を控えめに表示します
  - 軸を非表示にし、地図のみをクリーンに表示します
  - 10x10インチのサイズで、見やすい大きさを確保しています

技術的な実装の詳細：
- japanmap.picture() 関数を使用して基本的な地図データを生成
- matplotlib の imshow() 関数でグレースケール表示を実現
- tight_layout() で余白を自動調整し、表示領域を最適化

必要なライブラリ：
- japanmap: 日本地図の基本データと描画機能の提供
- matplotlib.pyplot: 地図の表示とカスタマイズ

出力：
白地図スタイルの日本地図（matplotlib ウィンドウに表示）
- 白色の背景
- グレースケールの境界線
- 軸や目盛りのない、すっきりとしたデザイン

使用シーン：
このプログラムが特に有用な場面：
1. 教育現場での白地図としての使用
2. プレゼンテーションの背景地図
3. 後から情報を追加するベースマップとして
4. シンプルなデザインが求められる出版物用の地図

カスタマイズのポイント：
1. figsize の値を変更することで、出力サイズを調整可能
2. alpha の値を変更することで、境界線の濃さを調整可能
3. cmap を変更することで、異なる色調の境界線を設定可能

注意事項：
- 地図の投影法は japanmap ライブラリの既定値を使用しています
- 都道府県名の表示には対応していないため、必要な場合は追加の実装が必要です
"""
import japanmap as jm
import matplotlib.pyplot as plt

# 日本地図を描画
plt.figure(figsize=(10, 10))

# 都道府県コードのリストを取得
pref_code = jm.pref_names  # これはリスト形式として返される

# 都道府県の色分けデータを作成（全て同じ色に設定）
# 'jm.pref_names' はインデックス（0から47）でアクセス可能
cmap = {i + 1: 'white' for i in range(len(pref_code))}  # 1 から 47 のコードに対応

# 境界線を薄く設定して地図を描画
plt.imshow(jm.picture(cmap),
           cmap='Greys',  # グレースケールのカラーマップを使用
           alpha=0.3)     # 透明度を設定

# 軸を非表示に
plt.axis('off')

# 余白を調整
plt.tight_layout()

plt.show()
